/**
 * Check if decision is pending
 */
export function isPending(decision: Decision): boolean {
  return decision.status === 'pending' && !isExpired(decision);
}

/**
 * Check if decision is expired
 */
export function isExpired(decision: Decision): boolean {
  if (!decision.expiresAt) return false;
  return new Date(decision.expiresAt) < new Date();
}

/**
 * Check if decision is actionable (pending and not expired)
 */
export function isActionable(decision: Decision): boolean {
  return isPending(decision) && !isExpired(decision);
}

/**
 * Check if decision is critical priority
 */
export function isCritical(decision: Decision): boolean {
  return decision.priority === 'critical';
}

/**
 * Get priority color
 */
export function getPriorityColor(priority: DecisionPriority): string {
  return PRIORITY_CONFIGS[priority].color;
}

/**
 * Get priority label
 */
export function getPriorityLabel(priority: DecisionPriority): string {
  return PRIORITY_CONFIGS[priority].label;
}

/**
 * Get decision type label
 */
export function getDecisionTypeLabel(type: DecisionType): string {
  return DECISION_TYPE_CONFIGS[type].label;
}

