/**
 * Decision Engine - TypeScript Interfaces
 * 
 * FASE 8: Decision Engine V1 (Hard-coded Rules)
 * 
 * Core types for the automated decision-making system that analyzes
 * athlete metrics and generates recommendations for coaches.
 * 
 * Flow:
 * 1. Gather athlete metrics (current + historical)
 * 2. Evaluate rules against metrics
 * 3. Generate decisions when conditions met
 * 4. Store decisions with priority & confidence
 * 5. Coach sees decisions in UI (Fase 9)
 * 6. Coach applies or dismisses decisions
 */

// ============================================================================
// DECISION TYPES
// ============================================================================

/**
 * Type of decision/recommendation
 */
export type DecisionType = 
  | 'reduce-load'        // Reduce training load
  | 'rest-day'           // Schedule rest day
  | 'alert'              // Monitor athlete closely
  | 'increase-load'      // Can increase load (future)
  | 'adjust-program'     // Modify training program
  | 'medical-evaluation' // Requires medical attention
  | 'nutrition-focus'    // Focus on nutrition/recovery (future)
  | 'mental-health';     // Mental health check (future)

/**
 * Priority level of decision
 */
export type DecisionPriority = 
  | 'critical'  // Immediate action required (red)
  | 'high'      // Action needed soon (orange)
  | 'medium'    // Monitor situation (yellow)
  | 'low';      // FYI only (blue)

/**
 * Current status of decision
 */
export type DecisionStatus = 
  | 'pending'    // Awaiting coach action
  | 'applied'    // Coach applied the recommendation
  | 'dismissed'  // Coach dismissed the recommendation
  | 'expired';   // Decision expired (time-based)

/**
 * Metric status color coding
 */
export type MetricStatus = 
  | 'green'   // Within healthy range
  | 'yellow'  // Warning zone
  | 'red';    // Critical zone

// ============================================================================
// CORE INTERFACES
// ============================================================================

/**
 * Metric used in decision-making
 * 
 * Provides context about which metrics influenced the decision
 * and their relative importance (weight).
 */
export interface MetricUsed {
  metricId: string;
  name: string;
  value: number | string;
  weight: number;              // 0-1 (importance in decision)
  status: MetricStatus;        // Visual indicator
  threshold?: number;          // Threshold that was crossed (optional)
  baseline?: number;           // Baseline value for comparison (optional)
}

/**
 * Decision generated by the engine
 * 
 * Represents an automated recommendation for a specific athlete
 * based on their current metrics and historical data.
 */
export interface Decision {
  id: string;
  workspaceId: string;
  athleteId: string;
  athleteName?: string;        // Denormalized for UI
  
  // Classification
  type: DecisionType;
  priority: DecisionPriority;
  status: DecisionStatus;
  
  // Content
  recommendation: string;      // Short actionable recommendation
  reasoning: string;           // Detailed explanation WHY
  
  // Context
  metricsUsed: MetricUsed[];   // Metrics that triggered this decision
  ruleId?: string;             // Which rule generated this (if from rule)
  confidence: number;          // 0-1 confidence score
  
  // Metadata
  createdAt: string;           // ISO timestamp
  expiresAt?: string;          // Auto-expire old decisions
  appliedAt?: string;          // When coach applied
  appliedBy?: string;          // Coach user ID
  dismissedAt?: string;        // When coach dismissed
  dismissedBy?: string;        // Coach user ID
  dismissReason?: string;      // Why dismissed (optional)
  notes?: string;              // Coach can add notes
  
  // Analytics (future)
  viewedAt?: string;           // When first viewed
  viewCount?: number;          // How many times viewed
}

/**
 * Rule definition for decision-making
 * 
 * Each rule evaluates metrics and generates decisions when
 * specific conditions are met.
 */
export interface DecisionRule {
  id: string;
  name: string;
  description: string;
  priority: DecisionPriority;
  
  // Evaluation logic
  conditions: (context: MetricContext) => boolean;
  generate: (context: MetricContext) => Omit<Decision, 'id' | 'workspaceId' | 'status' | 'createdAt'>;
  
  // Configuration
  enabled: boolean;
  cooldown?: number;           // Hours before same rule can trigger again for same athlete
  
  // Metadata
  category?: 'recovery' | 'load' | 'injury' | 'performance';
  version?: string;            // For tracking rule changes
  createdAt?: string;
  updatedAt?: string;
}

/**
 * Metric context for rule evaluation
 * 
 * Contains all metric data needed to evaluate rules for a single athlete.
 */
export interface MetricContext {
  athleteId: string;
  workspaceId: string;
  
  // Current metrics (most recent values)
  currentMetrics: Record<string, number>;
  
  // Historical data
  historicalMetrics: {
    last7d: Record<string, number[]>;   // Last 7 days per metric
    last30d: Record<string, number[]>;  // Last 30 days per metric
    baseline: Record<string, number>;   // 4-week baseline per metric
  };
  
  // Athlete profile (for context)
  athleteProfile?: {
    name: string;
    sport?: string;
    age?: number;
    gender?: string;
    position?: string;
  };
  
  // Timestamp when context was built
  contextBuiltAt?: string;
}

/**
 * Result of evaluating a single rule
 */
export interface RuleEvaluationResult {
  ruleId: string;
  ruleName: string;
  triggered: boolean;
  decision?: Decision;         // Only if triggered
  evaluatedAt: string;
  executionTime: number;       // Milliseconds
  error?: string;              // If evaluation failed
}

/**
 * Result of running entire engine for workspace
 */
export interface EngineRunResult {
  workspaceId: string;
  athletesEvaluated: number;
  rulesEvaluated: number;
  decisionsCreated: number;
  decisionsSkipped: number;    // Cooldown or duplicate
  errors: string[];
  warnings: string[];
  duration: number;            // Total milliseconds
  timestamp: string;
  
  // Detailed breakdown
  breakdown?: {
    byPriority: Record<DecisionPriority, number>;
    byType: Record<DecisionType, number>;
    byRule: Record<string, number>;
  };
}

