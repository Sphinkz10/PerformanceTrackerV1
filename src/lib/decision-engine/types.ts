/**
 * Metric value with timestamp
 */
export interface MetricValue {
  value: number;
  timestamp: string;
  source?: string;             // 'manual', 'form', 'integration'
}

/**
 * Aggregated metric statistics
 */
export interface MetricStats {
  metricId: string;
  athleteId: string;
  
  // Basic stats
  current: number;             // Most recent value
  min: number;
  max: number;
  avg: number;
  median: number;
  stdDev: number;
  
  // Trend analysis
  trend: 'increasing' | 'decreasing' | 'stable';
  changePercent: number;       // % change vs baseline
  
  // Time windows
  last7dAvg: number;
  last30dAvg: number;
  baseline: number;            // 4-week average
  
  // Computed at
  computedAt: string;
}

/**
 * ACWR (Acute:Chronic Workload Ratio) calculation
 */
export interface ACWRData {
  athleteId: string;
  acute: number;               // 7-day average
  chronic: number;             // 28-day average
  ratio: number;               // acute / chronic
  status: MetricStatus;        // green (0.8-1.3), yellow (1.3-1.5), red (>1.5 or <0.8)
  recommendation: string;
  computedAt: string;
}

