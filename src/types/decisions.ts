export type DecisionType = 
  | 'rest'
  | 'modify_load'
  | 'review_athlete'
  | 'medical_referral'
  | 'celebrate'
  | 'custom';

export type DecisionPriority = 
  | 'critical'
  | 'high'
  | 'medium'
  | 'low';

export type DecisionStatus = 
  | 'pending'
  | 'accepted'
  | 'rejected'
  | 'expired';

export type RuleConnector = 
  | 'AND'
  | 'OR'
  | null;

/**
 * NEW: Updated operators with baseline-aware options
 */
export type RuleOperator = 
  // Absolute comparisons
  | 'less-than' 
  | 'greater-than' 
  | 'equals'
  // NEW: Baseline-relative comparisons
  | 'below-baseline'      // X% below athlete's baseline
  | 'above-baseline'      // X% above athlete's baseline
  | 'outside-range';      // Outside ±X% of baseline

export const OPERATOR_LABELS: Record<RuleOperator, string> = {
  'less-than': 'Less than',
  'greater-than': 'Greater than',
  'equals': 'Equals',
  'below-baseline': 'Below baseline by',
  'above-baseline': 'Above baseline by',
  'outside-range': 'Outside baseline range',
};

export const OPERATOR_DESCRIPTIONS: Record<RuleOperator, string> = {
  'less-than': 'Value is below absolute threshold',
  'greater-than': 'Value is above absolute threshold',
  'equals': 'Value equals exact value',
  'below-baseline': 'Value is X% below athlete\'s personal baseline',
  'above-baseline': 'Value is X% above athlete\'s personal baseline',
  'outside-range': 'Value is outside ±X% of athlete\'s baseline',
};

