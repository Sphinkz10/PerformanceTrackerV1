// VALIDATION
// ============================================

export interface RuleValidationResult {
  valid: boolean;
  errors: string[];
  warnings?: string[];
}

export function validateRule(rule: Partial<BeliefRule>): RuleValidationResult {
  const errors: string[] = [];
  const warnings: string[] = [];
  
  // Basic validation
  if (!rule.name) errors.push('Name is required');
  if (!rule.decisionType) errors.push('Decision type is required');
  if (!rule.decisionTemplate) errors.push('Decision template is required');
  
  // Conditions validation
  if (!rule.conditions || rule.conditions.length === 0) {
    errors.push('At least one condition is required');
  } else {
    rule.conditions.forEach((condition, index) => {
      if (!condition.metricId) {
        errors.push(`Condition ${index + 1}: Metric is required`);
      }
      if (!condition.operator) {
        errors.push(`Condition ${index + 1}: Operator is required`);
      }
      if (condition.value === undefined || condition.value === null) {
        errors.push(`Condition ${index + 1}: Value is required`);
      }
      
      // Baseline operator validation
      const isBaselineOperator = ['below-baseline', 'above-baseline', 'outside-range'].includes(condition.operator);
      if (isBaselineOperator && !condition.absoluteValue) {
        warnings.push(`Condition ${index + 1}: No fallback value set - will skip if baseline unavailable`);
      }
    });
  }
  
  // Priority validation
  if (rule.priority !== undefined) {
    if (rule.priority < 0 || rule.priority > 250) {
      errors.push('Priority must be between 0 and 250');
    }
  }
  
  // Cooldown validation
  if (rule.cooldownHours !== undefined) {
    if (rule.cooldownHours < 0) {
      errors.push('Cooldown hours cannot be negative');
    }
    if (rule.cooldownHours > 168) {
      warnings.push('Cooldown > 1 week may cause rule to rarely trigger');
    }
  }
  
  return {
    valid: errors.length === 0,
    errors,
    warnings: warnings.length > 0 ? warnings : undefined,
  };
}

