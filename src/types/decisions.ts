/**
 * Check if rule is in cooldown
 */
export function isInCooldown(rule: BeliefRule): boolean {
  if (!rule.lastTriggered) return false;
  
  const cooldownEnd = new Date(rule.lastTriggered);
  cooldownEnd.setHours(cooldownEnd.getHours() + (rule.cooldownHours || 24));
  
  return new Date() < cooldownEnd;
}

/**
 * Get cooldown end time
 */
export function getCooldownEnd(rule: BeliefRule): Date | null {
  if (!rule.lastTriggered) return null;
  
  const cooldownEnd = new Date(rule.lastTriggered);
  cooldownEnd.setHours(cooldownEnd.getHours() + (rule.cooldownHours || 24));
  
  return cooldownEnd;
}

/**
 * Format rule condition as human-readable string
 */
export function formatCondition(condition: RuleCondition, metricName?: string): string {
  const metric = metricName || 'Metric';
  const operator = OPERATOR_LABELS[condition.operator];
  
  switch (condition.operator) {
    case 'below-baseline':
    case 'above-baseline':
      return `${metric} ${operator.toLowerCase()} ${condition.value}%`;
    
    case 'outside-range':
      return `${metric} ${operator.toLowerCase()} ±${condition.value}%`;
    
    default:
      return `${metric} ${operator.toLowerCase()} ${condition.value}`;
  }
}

/**
 * Get priority label
 */
export function getPriorityLabel(priority: number): DecisionPriority {
  if (priority >= RULE_PRIORITY_RANGES.CRITICAL.min) return 'critical';
  if (priority >= RULE_PRIORITY_RANGES.HIGH.min) return 'high';
  if (priority >= RULE_PRIORITY_RANGES.MEDIUM.min) return 'medium';
  return 'low';
}

/**
 * Convert rule to decision template
 */
export function renderDecisionTemplate(
  template: string,
  metrics: Record<string, { name: string; value: number; baseline?: number }>
): string {
  let rendered = template;
  
  Object.entries(metrics).forEach(([metricId, data]) => {
    rendered = rendered
      .replace(new RegExp(`{{${metricId}}}`, 'g'), String(data.value))
      .replace(new RegExp(`{{${metricId}_name}}`, 'g'), data.name)
      .replace(new RegExp(`{{${metricId}_baseline}}`, 'g'), String(data.baseline || 'N/A'));
  });
  
  return rendered;
}

