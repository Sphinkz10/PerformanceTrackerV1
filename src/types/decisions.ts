export interface BeliefRule {
  id: string;
  workspaceId: string;
  
  // Basic info
  name: string;
  description?: string;
  
  // Conditions
  conditions: RuleCondition[];
  
  // Decision output
  decisionType: DecisionType;
  decisionTemplate: string;  // Template with {{metric_name}} placeholders
  priority: number;           // 0-200 (higher = more important)
  
  // NEW: Cooldown to prevent spam
  cooldownHours: number;      // Min hours between triggers (default: 24)
  
  // Metadata
  createdBy: string;
  createdAt: string;
  updatedAt: string;
  isActive: boolean;
  
  // Stats
  triggerCount: number;
  lastTriggered?: string;
}

export interface RuleCondition {
  metricId: string;
  operator: RuleOperator;
  value: number;              // Percentage if baseline operator, absolute if not
  absoluteValue?: number;     // NEW: Fallback if no baseline available
  connector: RuleConnector;
}

